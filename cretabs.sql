CREATE TABLE CUSTOMER (
 CUSTID              varchar (45)  NOT NULL,
 NAME                varchar (45),
 ADDRESS             varchar (40),
 CITY                varchar (30),
 STATE               varchar (2),
 ZIP                 varchar (9),
 AREA                varchar (45) ,
 PHONE               varchar (9),
 REPID               varchar (45)  NOT NULL,
 CREDITLIMIT         varchar (45) ,
 COMMENTS            varchar (200),
 CONSTRAINT CUSTOMER_CUSTID_PK  PRIMARY KEY (CUSTID),
 CONSTRAINT CUSTOMER_CUSTID_CK CHECK (cast(CUSTID AS INTEGER) > 0));
CREATE TABLE ORD  (
 ORDID               varchar (45)  NOT NULL,
 ORDERDATE           DATE,
 COMMPLAN            varchar (1),
 CUSTID              varchar (45)  NOT NULL,
 SHIPDATE            DATE,
 TOTAL               varchar (45)  CONSTRAINT ORD_TOTAL_CK CHECK (cast(TOTAL AS DECIMAL(9,2)) >= 0),
 CONSTRAINT ORD_CUSTID_FK FOREIGN KEY (CUSTID) REFERENCES CUSTOMER (CUSTID),
 CONSTRAINT ORD_ORDID_PK  PRIMARY KEY (ORDID));
CREATE TABLE ITEM  (
 ORDID               varchar (45)  NOT NULL,
 ITEMID              varchar (45)  NOT NULL,
 PRODID              varchar (45) ,
 ACTUALPRICE         varchar (45) ,
 QTY                 varchar (45) ,
 ITEMTOT             varchar (45) ,
 CONSTRAINT ITEM_ORDID_FK FOREIGN KEY (ORDID) REFERENCES ORD (ORDID),
 CONSTRAINT ITEM_ORDID_ITEMID_PK  PRIMARY KEY (ORDID,ITEMID));
CREATE TABLE PRODUCT (
 PRODID              varchar (45)  CONSTRAINT PRODID_PK  PRIMARY KEY,
 DESCRIP             varchar (30));
CREATE TABLE PRICE (
 PRODID              varchar (45)  NOT NULL,
 STDPRICE            varchar (45) ,
 MINPRICE            varchar (45) ,
 STARTDATE           DATE,
 ENDDATE             DATE);
